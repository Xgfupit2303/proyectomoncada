let numero = parseInt(prompt("Ingrese el numero a analizar"))
if(numero % 3 == 0){
    console.log("El numero es divisible por 3")
}else{
    console.log("El numero no es divisible por 3")
}
if(numero % 5 == 0){
    console.log("El numero es divisible por 5")
}else{
    console.log("El numero no es divisible por 5")
}